#!/usr/bin/env bash
set -euo pipefail

REPO_URL="${PM_KIT_REPO_URL:-https://github.com/KilianThe14/claude-code-pm-kit.git}"
KIT_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]:-$0}")" 2>/dev/null && pwd)"

if [[ ! -d "$KIT_ROOT/skills" || ! -f "$KIT_ROOT/claude/CLAUDE.md" ]]; then
  tmp_dir="$(mktemp -d)"
  trap 'rm -rf "$tmp_dir"' EXIT
  git clone --depth 1 "$REPO_URL" "$tmp_dir/claude-code-pm-kit" >/dev/null
  bash "$tmp_dir/claude-code-pm-kit/install.sh" "$@"
  exit $?
fi

CLAUDE_HOME="${CLAUDE_HOME:-$HOME/.claude}"
BACKUP_ROOT="${PM_KIT_BACKUP_ROOT:-$HOME/.claude-pm-kit-backups}"
MANIFEST="$CLAUDE_HOME/.pm-kit-manifest"

mkdir -p "$CLAUDE_HOME" "$BACKUP_ROOT"

first_install=1
backup_dir="$BACKUP_ROOT/$(date +%Y%m%d-%H%M%S)"
if [[ -f "$MANIFEST" ]]; then
  first_install=0
  existing_backup="$(awk -F= '/^BACKUP_DIR=/{print $2}' "$MANIFEST" | tail -n 1)"
  if [[ -n "${existing_backup:-}" ]]; then
    backup_dir="$existing_backup"
  fi
fi
mkdir -p "$backup_dir"

backup_path() {
  local rel="$1"
  local dest="$CLAUDE_HOME/$rel"
  [[ "$first_install" -eq 1 && -e "$dest" ]] || return 0
  mkdir -p "$backup_dir/$(dirname "$rel")"
  cp -R -p "$dest" "$backup_dir/$rel"
}

install_file() {
  local src="$1"
  local rel="$2"
  local mode="${3:-0644}"
  backup_path "$rel"
  mkdir -p "$CLAUDE_HOME/$(dirname "$rel")"
  install -m "$mode" "$src" "$CLAUDE_HOME/$rel"
}

install_dir() {
  local src="$1"
  local rel="$2"
  backup_path "$rel"
  rm -rf "$CLAUDE_HOME/$rel"
  mkdir -p "$CLAUDE_HOME/$(dirname "$rel")"
  cp -R "$src" "$CLAUDE_HOME/$rel"
}

installed_paths=()
record_path() {
  installed_paths+=("$1")
}

install_file "$KIT_ROOT/claude/CLAUDE.md" "CLAUDE.md"
record_path "CLAUDE.md"

for dir in "$KIT_ROOT"/skills/*; do
  [[ -d "$dir" ]] || continue
  name="$(basename "$dir")"
  install_dir "$dir" "skills/$name"
  record_path "skills/$name"
done

for file in "$KIT_ROOT"/commands/*.md; do
  [[ -f "$file" ]] || continue
  name="$(basename "$file")"
  install_file "$file" "commands/$name"
  record_path "commands/$name"
done

for file in "$KIT_ROOT"/templates/*.md; do
  [[ -f "$file" ]] || continue
  name="$(basename "$file")"
  install_file "$file" "templates/$name"
  record_path "templates/$name"
done

for file in "$KIT_ROOT"/scripts/*.py; do
  [[ -f "$file" ]] || continue
  name="$(basename "$file")"
  install_file "$file" "scripts/$name" "0755"
  record_path "scripts/$name"
done

install_file "$KIT_ROOT/healthcheck.sh" "scripts/pm-kit-healthcheck.sh" "0755"
record_path "scripts/pm-kit-healthcheck.sh"

{
  echo "# Generated by claude-code-pm-kit. Do not edit by hand."
  echo "BACKUP_DIR=$backup_dir"
  echo "INSTALLED_AT=$(date -u +%Y-%m-%dT%H:%M:%SZ)"
  for path in "${installed_paths[@]}"; do
    echo "PATH=$path"
  done
} > "$MANIFEST"

echo "Claude Code PM Kit installed into $CLAUDE_HOME"
echo "Backup directory: $backup_dir"
echo "Run healthcheck: $CLAUDE_HOME/scripts/pm-kit-healthcheck.sh"
